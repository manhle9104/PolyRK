    private void btnThemNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemNhanVienActionPerformed
        // TODO add your handling code here:
        if (this.check()) {
            NhanVien nv = readForm();
            if (service.testTrung(nv.getID())) {
                JOptionPane.showMessageDialog(this, "trùng mã !");
            } else {

                if (service.insert_ST(nv) != 0) {
                    JOptionPane.showMessageDialog(this, "thêm thành công");
                    this.fillData(service.getAll());
                } else {
                    JOptionPane.showMessageDialog(this, "thêm thất bại");
                }

            }
        }
    }//GEN-LAST:event_btnThemNhanVienActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        index = tblNhanVien.getSelectedRow();
        String id = tblNhanVien.getValueAt(index, 0).toString();
        int a = JOptionPane.showConfirmDialog(this, "Ban co muon xoa");
        if (a == 0) {
            if (service.delete(id) != 0) {
                JOptionPane.showMessageDialog(this, "Xoa thanh cong");
                this.fillData(service.getAll());
            }else{
                JOptionPane.showMessageDialog(this, "Xoa that bai");
            }
        }

    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        index = tblNhanVien.getSelectedRow();
        NhanVien sv = this.readForm();
        String id = tblNhanVien.getValueAt(index, 0).toString();

        if (service.update(sv, id) > 0) {
            JOptionPane.showMessageDialog(this, "update thanh cong");
            this.fillData(service.getAll());
        } else {
            JOptionPane.showMessageDialog(this, "update that bai");
        }

    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed
        // TODO add your handling code here:

        String ma = txtTimNV.getText();

        if (service.find(ma) != null) {
            int check = 0;
            //            filltable(spctsv.findname(ma));
            for (int i = 0; i < tblNhanVien.getRowCount(); i++) {
                String values = tblNhanVien.getValueAt(i, 0).toString();
                if (values.trim().equals(ma)) {
                    index = i;
                    check++;
                    break;//thoat ngay
                }
            }
            if (check > 0) {
                this.showData(index);
                tblNhanVien.setRowSelectionInterval(index, index);
            } else {
                JOptionPane.showMessageDialog(this, "Khong tim thay");
            }
        } else {
            JOptionPane.showMessageDialog(this, "khong tim thay");
        }

    }//GEN-LAST:event_btnTimActionPerformed

