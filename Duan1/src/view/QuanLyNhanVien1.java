    void fillData(List<NhanVien> listnv) {
        model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        for (NhanVien nv : listnv) {
            model.addRow(nv.toDataRow());
        }
    }

    void showData(int index) {
        NhanVien nv = service.getAll().get(index);
        txtMa.setText(nv.getID());
        txtMatKhau.setText(nv.getMatkhau());
        txtTen.setText(nv.getHoTen());
        txtSdt.setText(nv.getSdt());
        cboVaiTro.setSelectedItem(nv.isVaiTro() ? "Nhan Vien" : "Quan Ly");

    }

    NhanVien readForm() {
        String id = txtMa.getText();
        String mk = new String( txtMatKhau.getPassword());
        String ten = txtTen.getText();
        String sdt = txtSdt.getText();
        Boolean vaitro;

        if (cboVaiTro.getSelectedIndex() == 0) {
            vaitro = false;
        } else {
            vaitro = true;
        }

        return new NhanVien(id, ten, sdt, vaitro, mk);
    }

    boolean check() {

        if (txtMa.getText().isEmpty() || txtTen.getText().isEmpty() || String.valueOf(txtMatKhau.getPassword()).isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin");
            return false;
        }
        return true;

    }

